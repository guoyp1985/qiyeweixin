    cutdown (item, interval) {
      interval = setInterval(function () {
        let h = parseInt(item.lefthour)
        let m = parseInt(item.leftminute)
        let s = parseInt(item.leftsecond)
        if (s > 0) {
          s--
          if (s < 10) {
            item.leftsecond = '0' + s
          } else {
            item.leftsecond = s
          }
        } else if (m > 0) {
          m--
          if (m < 10) {
            item.leftminute = '0' + m
          } else {
            item.leftminute = m
          }
          item.leftsecond = '59'
        } else if (h > 0) {
          h--
          if (h < 10) {
            item.lefthour = '0' + h
          } else {
            item.lefthour = h
          }
          item.leftminute = '59'
          item.leftsecond = '59'
        }
        if (h === 0 && m === 0 && s === 0) {
          clearInterval(interval)
        }
      }, 1000)
    },
