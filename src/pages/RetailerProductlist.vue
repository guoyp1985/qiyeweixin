<script>
import { TransferDom, Popup, Confirm, CheckIcon, XImg } from 'vux'
import ENV from 'env'
import { User } from '#/storage'

let pageStart1 = 0
let pageStart2 = 0
const limit = 10
export default {
  directives: {
    TransferDom
  },
  components: {
    Popup, Confirm, CheckIcon, XImg
  },
  data () {
    return {
      loginUser: {},
      query: {},
      productdata: [],
      controldata1: [
        { key: 'edit', title: '编辑' },
        { key: 'up', title: '上架' },
        { key: 'down', title: '下架' },
        { key: 'stat', title: '统计' },
        { key: 'createposter', title: '生成海报' }
      ],
      showpopup1: false,
      clickdata: {},
      clickindex: 0,
      limit: 10,
      showpush: false,
      customerdata: [],
      pushdata: [],
      checkAll: false,
      disproductdata: false,
      discustomerdata: false,
      showFeePopup: false,
      feeData: {},
      postFee: '0.00'
    }
  },
  watch: {
    productdata: function () {
      return this.productdata
    }
  },
  computed: {
    getquery: function () {
      return this.$route.query
    }
  },
  methods: {
    getPhoto (src) {
      return this.$util.getPhoto(src)
    },
    handleScroll: function (refname, type) {
      const self = this
      const scrollarea = self.$refs[refname][0] ? self.$refs[refname][0] : self.$refs[refname]
      self.$util.scrollEvent({
        element: scrollarea,
        callback: function () {
          if (type === 'product') {
            if (self.productdata.length === (pageStart1 + 1) * limit) {
              pageStart1++
              self.$vux.loading.show()
              self.getData1()
            }
          } else if (type === 'customer') {
            if (self.customerdata.length === (pageStart2 + 1) * limit) {
              pageStart2++
              self.$vux.loading.show()
              self.getCustomerdata()
            }
          }
        }
      })
    },
    controlpopup1 (item, index) {
      event.preventDefault()
      this.showpopup1 = !this.showpopup1
      this.clickdata = item
      this.clickindex = index
    },
    clickpopup (key) {
      const self = this
      if (key === 'up') {
        self.$vux.confirm.show({
          title: '确定要上架该商品吗？',
          onConfirm () {
            self.$vux.loading.show()
            let params = { id: self.clickdata.id, moderate: 1 }
            self.$http.post(`${ENV.BokaApi}/api/moderate/product`, params).then(function (res) {
              let data = res.data
              self.$vux.loading.hide()
              self.$vux.toast.show({
                text: data.error,
                type: (data.flag !== 1 ? 'warn' : 'success'),
                time: self.$util.delay(data.error),
                onHide: function () {
                  if (data.flag === 1) {
                    self.clickdata.moderate = 1
                    self.productdata[self.clickindex].moderate = 1
                    self.showpopup1 = false
                  }
                }
              })
            })
          }
        })
      } else if (key === 'down') {
        self.$vux.confirm.show({
          title: '确定要下架该商品吗？',
          onConfirm () {
            self.$vux.loading.show()
            let params = { id: self.clickdata.id, moderate: 0 }
            self.$http.post(`${ENV.BokaApi}/api/moderate/product`, params).then(function (res) {
              let data = res.data
              self.$vux.loading.hide()
              self.$vux.toast.show({
                text: data.error,
                type: (data.flag !== 1 ? 'warn' : 'success'),
                time: self.$util.delay(data.error),
                onHide: function () {
                  if (data.flag === 1) {
                    self.clickdata.moderate = 0
                    self.productdata[self.clickindex].moderate = 0
                    self.showpopup1 = false
                  }
                }
              })
            })
          }
        })
      } else if (key === 'edit') {
        self.showpopup1 = false
        self.$router.push({ path: '/addProduct', query: { id: self.clickdata.id } })
      } else if (key === 'push') {
        self.showpopup1 = false
        self.showpush = true
        if (self.customerdata.length === 0) {
          self.getCustomerdata()
        }
      } else if (key === 'fee') {
        self.showpopup1 = false
        self.showFeePopup = true
        self.feeData = self.clickdata
        self.postFee = self.feeData.rebate
      } else {
        self.showpopup1 = false
      }
    },
    closeFeePopup () {
      this.showFeePopup = false
    },
    submitFee () {
      const self = this
      if (self.$util.trim(self.postFee) === '') {
        self.$vux.toast.show({
          text: '请输入佣金'
        })
        return false
      }
      if (isNaN(self.postFee)) {
        self.$vux.toast.show({
          text: '请输入正确的佣金'
        })
        return false
      }
      self.$http.post(`${ENV.BokaApi}/api/retailer/updateRebate`, {
        id: self.clickdata.id, rebate: self.postFee
      }).then(function (res) {
        let data = res.data
        self.$vux.loading.hide()
        self.$vux.toast.show({
          text: data.error,
          type: data.flag !== 1 ? 'warn' : 'success',
          time: self.$util.delay(data.error),
          onHide: function () {
            if (data.flag === 1) {
              self.showFeePopup = false
              self.productdata[self.clickindex] = data.data
            }
          }
        })
      })
    },
    getCustomerdata () {
      const self = this
      self.$vux.loading.show()
      let params = { params: { pagestart: pageStart2, limit: limit } }
      self.$http.get(`${ENV.BokaApi}/api/retailer/sellersList`, params).then(function (res) {
        let data = res.data
        self.$vux.loading.hide()
        let retdata = data.data ? data.data : data
        self.customerdata = self.customerdata.concat(retdata)
        self.discustomerdata = true
      })
    },
    closepush () {
      this.showpush = false
    },
    submitpush () {
      const self = this
      if (self.pushdata.length === 0) {
        self.$vux.toast.show({
          text: '请选择返点客'
        })
        return false
      }
      self.showpush = false
      self.$vux.loading.show()
      let subdata = { id: self.clickdata.id, sendmodule: 'product', uid: self.pushdata }
      self.$http.post(`${ENV.BokaApi}/api/retailer/sendGroupNews`, subdata).then(function (res) {
        let data = res.data
        self.$vux.loading.hide()
        self.$vux.toast.show({
          text: data.error,
          time: self.$util.delay(data.error)
        })
      })
    },
    radioclick (data, index) {
      const self = this
      if (data.checked) {
        self.pushdata.push(data.uid)
      } else {
        self.checkAll = false
        for (let i = 0; i < self.pushdata.length; i++) {
          if (self.pushdata[i] === data.uid) {
            self.pushdata.splice(i, 1)
            break
          }
        }
      }
    },
    checkAllEvent () {
      const self = this
      for (let i = 0; i < self.customerdata.length; i++) {
        if (self.checkAll) {
          self.customerdata[i].checked = true
        } else {
          delete self.customerdata[i].checked
        }
      }
    },
    getData1 () {
      const self = this
      const params = { params: { pagestart: pageStart1, limit: limit } }
      this.$http.get(`${ENV.BokaApi}/api/list/product?from=retailer`, params)
      .then(res => {
        self.$vux.loading.hide()
        const data = res.data
        const retdata = data.data ? data.data : data
        self.productdata = self.productdata.concat(retdata)
        self.disproductdata = true
      })
    },
    init () {
      this.$vux.loading.show()
      this.$http.post(`${ENV.BokaApi}/api/retailer/logAction`, {
        module: 'retailer', action: 'productlist'
      })
    },
    refresh () {
      this.$store.commit('updateToggleTabbar', {toggleTabbar: false})
      this.loginUser = User.get()
      this.query = this.$route.query
      if (this.productdata.length < limit) {
        this.disproductdata = false
        this.productdata = []
        this.$vux.loading.show()
        pageStart1 = 0
        this.getData1()
      }
    }
  },
  created () {
    this.init()
  },
  activated () {
    this.refresh()
  }
}
</script>

